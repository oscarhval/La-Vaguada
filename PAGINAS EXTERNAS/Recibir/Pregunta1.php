            <h1>¿Qué es el aviso de llegada?</h1>
            <p>El Aviso de Llegada es un documento informativo para el destinatario de un envío. El cartero lo depositará en el buzón si el destinatario no se encuentra en el domicilio en el momento de la entrega.<br><br><br>

                En la parte frontal del Aviso, en “Datos del envío” se encuentra el número de envío, cuál es su origen, quién es el remitente y la fecha en la que se ha intentado realizar la entrega. El envío estará disponible en la oficina a partir del siguiente día laborable.<br><br><br>
                
                En “Situación del envío” se muestra si el envío se volverá enviar o si se deberá recoger en la oficina de Correos.<br><br><br>
                
                En “Modalidad” se indica el tipo de envío recibido: ordinario, urgente, con reembolso, etc.<br><br><br>
                
                En función del tipo de “Producto”, (Carta, Paquete Azul, Telegrama…) también se especifica el tiempo de permanencia del envío en la oficina. Finalizado este plazo, el envío se devolverá al remitente.<br><br><br>

                Por otra parte, en la parte trasera del Aviso, en “Oficina” se indican los datos de la sucursal en la que se debe recoger el envío: dirección, horario y teléfono de contacto.<br><br><br>
                
                Y “Requisitos para la recogida en oficina” es el apartado que se debe cumplimentar para autorizar que una persona recoja el envío en el caso de no poder recogerlo personalmente. Además del aviso, la persona autorizada debe presentar su DNI y una fotocopia del destinatario. Esta autorización será válida en todas las modalidades de entrega excepto en la de Entrega Exclusiva al Destinatario.</p>
