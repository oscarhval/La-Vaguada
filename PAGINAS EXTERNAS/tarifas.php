<h1>LISTADO ORDENADO DE TARIFAS DE LA EMPRESA</h1>
<p>Las tarifas internacionales se refieren a los costos asociados con servicios postales, mensajería o envíos de mercancías entre diferentes países. 
    Estas tarifas pueden variar significativamente según diversos factores, entre ellos:</p>
    <ul class="ul-body">
    <li>Peso y Dimensiones: El peso y tamaño del paquete suelen ser factores determinantes en las tarifas internacionales. 
        Los servicios postales y las empresas de mensajería suelen tener tablas de tarifas basadas en el peso y dimensiones del envío.</li>
        <li>Destino y Origen: La distancia entre el país de origen y el país de destino puede influir en las tarifas. 
            Los envíos a destinos más lejanos a menudo tienen tarifas más altas.</li>
    <li>Tipo de Servicio: Los servicios de envío express suelen ser más costosos que los servicios estándar. 
        La rapidez y la garantía de entrega pueden afectar el costo del servicio.</li>
    <li>Tipo de Contenido: Algunos tipos de mercancías pueden estar sujetos a restricciones y regulaciones específicas. 
        Envíos de productos peligrosos o de alto valor pueden tener tarifas adicionales.</li>
    <li>Aduanas e Impuestos: Los envíos internacionales están sujetos a inspecciones aduaneras y pueden estar sujetos a impuestos e aranceles. 
        Estos costos adicionales pueden influir en el costo total del envío</li>
</ul>
