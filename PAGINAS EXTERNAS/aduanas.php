 <h1 class="h1aduanas">Gestión aduanera DUA</h1>
<p>
  La gestión aduanera DUA se refiere al control aplicado a envíos que ingresan o salen de España desde países fuera de la Unión Europea, Canarias, Ceuta y Melilla. 
  Se encargan de formalizar el documento DUA necesario para acreditar la entrada o salida de mercancías. Los envíos internacionales están sujetos a controles aduaneros y pueden requerir el pago de impuestos. 
  Se aconseja informarse sobre restricciones, presentar los documentos necesarios y pagar impuestos para la liberación de paquetes.</p>
  <h3>DUA Importación</h3>
<p>A la hora de gestionar tu correspondencia, debes saber que todos los envíos que entran en España desde un país que no pertenezca a la Unión Europea o venga de Canarias, Ceuta y Melilla, estarán sujetos a controles por parte de la Aduana. Incluso las mercancías compradas a través de Internet estarán sometidas a este proceso de control, así como al pago de importes aduaneros. <br>  
Recuerda que si vas a recibir un envío internacional tienes la obligación de informarte sobre la mercancía que está restringida o prohibida en España, 
así como de presentar los documentos que sean necesarios para permitir su entrada en el país. <br> 
  Las mercancías importadas pueden estar sujetas a diferentes impuestos y derechos arancelarios según cuál sea su contenido, 
  valor y otros factores contemplados legalmente. Estos importes deberán ser abonados por el destinatario del envío para poder recoger los paquetes pendientes de tramitación aduanera. <br> 
  Determinados artículos pueden estar restringidos o prohibidos, dependiendo de la legislación nacional y de los países de destino. 
  La persona que realiza el envío es la responsable de informarse sobre las condiciones de importación y exportación, así como de facilitar los documentos requeridos para la salida 
  o entrada en el país de destino. No obstante, desde Correos te ofrecemos el asesoramiento que necesites para enviar o recibir tus envíos.<br> 
  Por otra parte, la importación en el territorio nacional de mercancía procedente de terceros países puede estar sujeta a controles de seguridad, 
  según el tipo de mercancía que contenga, más allá de los trámites que se deban realizar ante la Aduana para poder entregar el envío en tu domicilio. Asimismo, pueden requerir 
  el pago de los tributos obligatorios según la Ley.<br> 
  Si tu empresa exporta mercancía por vía postal puedes elegir que sea Correos quien realice los trámites de importación y exportación como tu representante ante la Aduana.</p>
  <h3>DUA Exportación</h3>
  <p>Los paquetes con destino a Canarias, Ceuta y Melilla o fuera de la Unión Europea (UE) pueden estar sujetos a determinados trámites aduaneros previos a su exportación por razones 
    de seguridad internacional, según la legislación aduanera vigente. Es posible que se requiera de cierta información adicional para este tipo de envíos que permita agilizar los 
    trámites en las Aduanas, así como facilitar una mayor trazabilidad. <br>
 Asimismo, todos los envíos postales con mercancías procedentes de Canarias, Ceuta y Melilla o países fuera de Europa están sujetos a controles fronterizos de seguridad y 
 a determinados trámites de importación, necesarios para liberar los envíos de la Aduana y recibirlos en tu domicilio.<br>
  Para tus envíos de Paquetería Comercial desde Península y Baleares hacia Canarias, Ceuta, Melilla y países no pertenecientes a la Unión Europea:</p><br>
